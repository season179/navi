function previewInitialState(mode: ClawAddImDialogPreviewMode): {
  dialogMode: ClawImDialogMode
  manageStage: ClawManageStage
  activeStep: ClawDialogStep
  channels: ClawImChannelSidebarSnapshot[]
  selectedChannelId: string
  provider: ClawImProvider
  installQrStatus: ClawInstallQrStatus
  installQrTimeLeft: number
  installQrUserCode: string
  installQrError: string
  error: string | null
  busy: boolean
  advancedOpen: boolean
} {
  if (mode === 'manage') {
    return {
      dialogMode: 'edit',
      manageStage: 'select',
      activeStep: 'defaults',
      channels: PREVIEW_CHANNELS,
      selectedChannelId: 'feishu-team',
      provider: 'feishu',
      installQrStatus: 'idle',
      installQrTimeLeft: 0,
      installQrUserCode: '',
      installQrError: '',
      error: null,
      busy: false,
      advancedOpen: false,
    }
  }
  if (mode === 'edit') {
    return {
      dialogMode: 'edit',
      manageStage: 'configure',
      activeStep: 'defaults',
      channels: PREVIEW_CHANNELS,
      selectedChannelId: 'feishu-team',
      provider: 'feishu',
      installQrStatus: 'success',
      installQrTimeLeft: 0,
      installQrUserCode: '',
      installQrError: '',
      error: null,
      busy: false,
      advancedOpen: false,
    }
  }
  if (mode === 'empty') {
    return {
      dialogMode: 'add',
      manageStage: 'select',
      activeStep: 'defaults',
      channels: PREVIEW_CHANNELS,
      selectedChannelId: '',
      provider: 'feishu',
      installQrStatus: 'idle',
      installQrTimeLeft: 0,
      installQrUserCode: '',
      installQrError: '',
      error: null,
      busy: false,
      advancedOpen: false,
    }
  }
  if (mode === 'manageEmpty') {
    return {
      dialogMode: 'edit',
      manageStage: 'select',
      activeStep: 'defaults',
      channels: [],
      selectedChannelId: '',
      provider: 'feishu',
      installQrStatus: 'idle',
      installQrTimeLeft: 0,
      installQrUserCode: '',
      installQrError: '',
      error: null,
      busy: false,
      advancedOpen: false,
    }
  }
  return {
    dialogMode: 'add',
    manageStage: 'select',
    activeStep:
      mode === 'prompt'
        ? 'prompt'
        : mode === 'connection' ||
            mode === 'qrLoading' ||
            mode === 'qrShowing' ||
            mode === 'qrSuccess' ||
            mode === 'qrError'
          ? 'connection'
          : 'defaults',
    channels: [],
    selectedChannelId: 'feishu-team',
    provider: 'feishu',
    installQrStatus:
      mode === 'qrLoading'
        ? 'loading'
        : mode === 'qrShowing'
          ? 'showing'
          : mode === 'qrSuccess'
            ? 'success'
            : mode === 'qrError'
              ? 'error'
              : 'idle',
    installQrTimeLeft: mode === 'qrShowing' ? 87 : 0,
    installQrUserCode: mode === 'qrShowing' ? 'ABCD-1234' : '',
    installQrError:
      mode === 'qrError'
        ? 'WeChat login service is unavailable. Restart the app and try again.'
        : '',
    error: mode === 'error' ? 'Could not save IM connection settings.' : null,
    busy: mode === 'busy',
    advancedOpen: mode === 'connection',
  }
}

