function MockQrCode(): ReactElement {
  return (
    <svg viewBox="0 0 48 48" className="claw-add-im-mock-qr" aria-hidden="true">
      <rect width="48" height="48" fill="#fff" />
      <rect x="4" y="4" width="12" height="12" fill="#111" />
      <rect x="32" y="4" width="12" height="12" fill="#111" />
      <rect x="4" y="32" width="12" height="12" fill="#111" />
      {Array.from({ length: 64 }, (_, index) => {
        const row = Math.floor(index / 8)
        const col = index % 8
        if ((row < 2 && col < 2) || (row < 2 && col > 5) || (row > 5 && col < 2)) return null
        if ((row + col + index) % 3 === 0) {
          return (
            <rect
              key={index}
              x={16 + col * 2}
              y={16 + row * 2}
              width="2"
              height="2"
              fill="#111"
            />
          )
        }
        return null
      })}
    </svg>
  )
}

function ToggleSwitch({
  checked,
  onChange,
  ariaLabel,
}: {
  checked: boolean
  onChange: (next: boolean) => void
  ariaLabel: string
}): ReactElement {
  return (
    <button
      type="button"
      className={`claw-add-im-toggle${checked ? ' is-on' : ''}`}
      aria-label={ariaLabel}
      aria-pressed={checked}
      onClick={() => onChange(!checked)}
    >
      <span className="claw-add-im-toggle-thumb" />
    </button>
  )
}

